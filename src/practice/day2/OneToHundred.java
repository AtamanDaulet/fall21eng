public class OneToHundred {
	/*
	 * print on the console numbers from 1 to 100 do not use any number!
	 *
	 */
	public static void main(String[] args) {

		int n1 = 'a'/'a';
		int n2 = 'd';
		
		for (int i = n1; i <= n2; i++) {
			System.out.println("Number:"+i);
		}

		
	}

}
